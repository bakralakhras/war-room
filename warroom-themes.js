  function applyWarroomTheme(id) {
    const theme = getTheme(id);
    const root = document.documentElement;
    const vars = Object.assign({}, THEMES[0].vars, theme.vars);
    Object.keys(vars).forEach(key => root.style.setProperty(key, vars[key]));
    root.dataset.theme = theme.id;
    document.body.classList.toggle('dense', theme.density === 'dense');
    document.body.classList.toggle('airy', theme.density === 'airy');
    return theme;
  }

